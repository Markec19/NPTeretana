    /**
     * Popunjva formu podacima
     */
    private void popuniFormu() {
        jcbGrad.setSelectedIndex(-1);
        
        List<Grad> gradovi = new ArrayList<>();
        
        try {
            gradovi = Controller.getInstance().vratiSveGradove();
            jcbGrad.setModel(new DefaultComboBoxModel(gradovi.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(FrmPregledTeretana.class.getName()).log(Level.SEVERE, null, ex);
        }
        popuniTabelu();
    }
    
    /**
     * Popunjva tabelu teretana
     */
    private void popuniTabelu() {
        try {
            teretane = Controller.getInstance().vratiSveTeretane();
            tblTeretane.setModel(new TeretanaTableModel(teretane));
        } catch (Exception ex) {
            Logger.getLogger(FrmTrener.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
